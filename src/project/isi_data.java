    private void Read_data(){
        Statement sttRead;
        ResultSet rssRead;
        String barang = String.valueOf(combobarang.getSelectedItem());
        String layanan = String.valueOf(combolayanan.getSelectedItem());
        double  hargabarang, hargalayanan,tmp;
        double  kuanti;
        if(kuantitas.getText().equals("")){
            kuanti = 0;
        }else{
            kuanti = Double.parseDouble(kuantitas.getText());
        }
        
        int id_bar,id_lay;
                
        try{    
            sttRead = con.createStatement();
            String sqlRead = "SElECT * FROM barang,layanan where nama_barang='"
                    +barang+"' and nama_layanan='"+layanan+"'";
            rssRead = sttRead.executeQuery(sqlRead);
            
            rssRead.next();
               hargabarang = Double.parseDouble(rssRead.getString("harga_barang"));
               hargalayanan = Double.parseDouble(rssRead.getString("harga_layanan"));
               id_bar = rssRead.getInt("id_barang");
               id_lay = rssRead.getInt("id_layanan");
               totalbayar = (hargabarang*hargalayanan)*kuanti;               
               total.setText(String.valueOf(totalbayar));
            //Hitung Diskon jika berat lebih dari 10  
            if(kuanti>=10){
                disc = true;
                tmp = (totalbayar*10)/100;//Diskon 10%
                totalbayar= totalbayar-tmp;
                diskon.setText(String.valueOf(totalbayar));
            }else{
                diskon.setText("0");
            }
            if(check_selesai){
                totalbayar = Double.parseDouble(Total_Harga_Transaksi.getText());
            input_data(id_bar, id_lay, totalbayar);
            }
        }catch(SQLException e){
            System.out.println("Masalah \n "+ e.getMessage());
        }   
    }
