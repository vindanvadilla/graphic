    private void UbahFont(){
        Font biasa = new Font ("Verdana", Font.PLAIN,11);
        Font fontawal = null;
        if(form_jln.isFocusOwner()){
            fontawal = form_jln.getFont();
            if(!biasa.equals(fontawal)){
                form_jln.setText("");
                form_jln.setFont(biasa);
                form_jln.setForeground(Color.black);
            }
        }else
        if(form_kec.isFocusOwner()){
            fontawal = form_kec.getFont();
            if(!biasa.equals(fontawal)){
                form_kec.setText("");
                form_kec.setFont(biasa);
                form_kec.setForeground(Color.black);
            }
        }else
        if(form_kota.isFocusOwner()){
            fontawal = form_kota.getFont();
            if(!biasa.equals(fontawal)){
                form_kota.setText("");
                form_kota.setFont(biasa);
                form_kota.setForeground(Color.black);
            }
        }
    }
